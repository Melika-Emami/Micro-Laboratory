			uint32_t val=0;
			
			for(int i=0;i < N; i++)
				val+=buffer[i];
			float fval=(float)val/(float)N;
			float temp = convert(fval);
			
			variance = var(fval);
			med = median(buffer, N);
			sprintf(line,"sample=%d , temprature=%f , var=%f , median = %f",val,temp,variance,med);
			
			
