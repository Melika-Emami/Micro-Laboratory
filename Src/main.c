float median(uint16_t* buffer, int N) 
{
  int swapped = 1;
  int j = 0;
	uint32_t temp;
 
  while (swapped) 
	{
    swapped = 0;
    j++;
    for (int i = 0; i < N - j; i++) {
      if (buffer[i] > buffer[i + 1]) {
        temp = buffer[i];
        buffer[i] = buffer[i + 1];
        buffer[i + 1] = temp;
        swapped = 0;
      }
    }
  }
	if(N %2 == 0){
		temp = (buffer[N/2] + buffer[(N/2) +1])/2;
	}
	else
		temp = buffer[(N+1)/2];
	return convert(temp);
}

float var (float fval)
{
	float var = 0.0;
	for(int i=0;i < N; i++)
	{
		var = pow(((float)buffer[i] - fval),2) + var;
	}
	var = (var/(float)N);
	var = pow(var,0.5);
	var=convert(var);
	return var;
}

